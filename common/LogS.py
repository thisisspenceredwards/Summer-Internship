class LogS(LogBase):

    def __init__(self, systemLogger):
        super().__init__(systemLogger)
        return


    def debugs(self, msg, *args, **kwargs):
        self._logger.debug(msg, *args, **kwargs)
        return

    def infos(self, msg, *args, **kwargs):
        self._logger.info(msg, *args, **kwargs)
        return

